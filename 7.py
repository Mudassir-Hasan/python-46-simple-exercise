# QUESTION :

# 7. Define a function reverse()that computes the reversal of a string. For example, reverse("I am
# testing")should return the string "gnitset ma I".

# Solution :

def reversal(string): 
    
    print(f'The reversal of a given string {string} is \"{string[ ::-1]} \" ') 

# inputing a string

string = input('Enter a string to get its reversal : ')  

# calling a function 

reversal(string) 


    